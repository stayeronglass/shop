    public function getMainCategories(){
        return $this->createQueryBuilder('p')
            ->andWhere('p.main = :main')
            ->setParameter('main', 1)
            ->orderBy('p.id', 'ASC')
            ->getQuery()
            ->getResult(Query::HYDRATE_ARRAY);
